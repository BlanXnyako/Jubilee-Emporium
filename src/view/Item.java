package view;

public class Item {

	String itemName;
	Integer itemPrice;
	Integer itemStock;
	String itemDesc;
	Integer itemInv;
	
	public Item(String itemName, Integer itemPrice, Integer itemStock, String itemDesc, Integer itemInv) {
		super();
		this.itemName = itemName;
		this.itemPrice = itemPrice;
		this.itemStock = itemStock;
		this.itemDesc = itemDesc;
		this.itemInv = itemInv;
	}
	
	
}
